let typingDelay = 35
const windowWidth = (window.innerWidth > 0) ? window.innerWidth : screen.width;
if (windowWidth < 600) { // slower typing speed for mobile
    typingDelay = 50
}

let timeoutHandles = [];

function addMessage(user, text) {
    const chatContainer = document.getElementById('chatContainer');
    const messageDiv = document.createElement('div');
    messageDiv.classList.add('message');
    const labelDiv = document.createElement('div');
    labelDiv.classList.add('label');
    // Create and append the icon span to the label
    const iconSpan = document.createElement('span');
    iconSpan.classList.add('icon');
    iconSpan.textContent = user === "Human" ? "🙂" : "🤖"; // Adding emoji icons for Human and Assistant
    labelDiv.appendChild(iconSpan);
    labelDiv.appendChild(document.createTextNode(" " + user)); // Space added for separation

    const textSpan = document.createElement('span');
    textSpan.classList.add('text');
    textSpan.textContent = text;
    messageDiv.appendChild(labelDiv);
    messageDiv.appendChild(textSpan);
    chatContainer.appendChild(messageDiv);
    return textSpan;
}

function typeMessage(message, element, index = 0, callback) {
    if (index < message.length) {
        const handle = setTimeout(() => {
            element.textContent += message[index];
            typeMessage(message, element, index + 1, callback);
            // Automatically scroll down to the latest message
            chatContainer.scrollTop = chatContainer.scrollHeight;
        }, typingDelay);
        timeoutHandles.push(handle);
    } else if (callback) {
        callback();
    }
}

function simulateConversation(conversations, index = 0, delaySoFar = 0) {
    if (index < conversations.length) {
        const { user, text } = conversations[index];
        const delay = text.length + 50;
        const handle = setTimeout(() => {
            const messageElement = addMessage(user, ' ');
            typeMessage(text, messageElement, 0, () => {
                simulateConversation(conversations, index + 1, delay);
            });
        }, delaySoFar);
        timeoutHandles.push(handle);
    }
}

function startSpinning() {
    const spinButton = document.getElementById('spinButton');
    spinButton.classList.add('spinner');
    setTimeout(() => {
        spinButton.classList.remove('spinner');
        startRandomConversation();
    }, 1000); // Spin animation lasts for 1 second
}

function hideClickMeMessage() {
    var clickMeMessage = document.getElementById('clickMeMessage');
    if (clickMeMessage) {
        clickMeMessage.style.display = 'none';
    }
}

